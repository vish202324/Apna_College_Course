// Arithmetic Operatos
let a = 10;
let b = 5;
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);

// Unary Operators
console.log(a++); //First Assigned and print 10 then update: 11
console.log(++a); //First increase then assign: 12

// comparison values(will give you true. because it's compare only values not their types)
console.log(b == "5");

//comparison values as well as their types. it will give false. called strict comparison
console.log(a === "5");
