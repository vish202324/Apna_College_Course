// Function Expression
// यहां function() को एक anonymous function (जिसका नाम नहीं होता)
// के रूप में variable "sum" में store किया गया है।
let sum = function (a, b) {
  return a + b;
};

console.log(sum(1, 2)); // calling and printing the o/p of the function

