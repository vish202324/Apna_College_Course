let size = "X";
if (size === "XL") {
  console.log("Price is 250 Rs.");
} else if (size === "L") {
  console.log("Price is 200 Rs.");
} else if (size === "M") {
  console.log("Price is 100 Rs.");
} else if (size === "S") {
  console.log("Price is 50 Rs.");
} else {
  console.log("Please choose the correct size!");
}
